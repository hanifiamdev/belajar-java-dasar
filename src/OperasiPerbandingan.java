public class OperasiPerbandingan {
    public static void main(String[] args) {
        /*
        * Operasi perbandingan adalah operasi yang membandingkan dua buah data dengan menghasilkan nilai true jika benar dan false jika salah
        * > / Lebih Dari
        * < / Kurang Dari
        * >= / Lebih Dari Sama Dengan
        * <= / Kurang Dari Sma Dengan
        * == / Sama Dengan
        * != / Tidak Samam Dengan
        * */
        int a = 100;
        int b = 100;

        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
        System.out.println("a >= b : " + (a >= b));
        System.out.println("a <= b : " + (a <= b));
        System.out.println("a == b : " + (a == b));
        System.out.println("a != b : " + (a != b));
    }
}
