public class TipeDataNumber {

    public static void main(String[] args) {
        // Integer Number
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 10000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000L;

        // Floating Point Number
        float iniFloat = 10.10F;
        double iniDouble = 10.10;

        // Literals
        int decimalInt = 25;
        // tambahkan didepannya 0x diikuti angka hexadecimalnya
        int hexDecimal = 0xFFFFFF;
        // tambahkan didepannya 0b diikuti angka binaryDecimalnya
        int binaryDecimal = 0b10101010;

        /*Sejak versi java 8 dapat menggunakan underscore untuk memudahkan pembacaan*/
        int amount = 1_000_000_000;

    }
}
